proc print_suit(suit:Suit) =
    var s0,s1,s2,s3,s4,s5,s6,s7,s8,s9 = "  "
    for card in full_deck:
        if card.standard_suit == suit:
            s0.add(card.art0 & "  ")
            s1.add(card.art1 & "  ")
            s2.add(card.art2 & "  ")
            s3.add(card.art3 & "  ")
            s4.add(card.art4 & "  ")
            s5.add(card.art5 & "  ")
            s6.add(card.art6 & "  ")
            s7.add(card.art7 & "  ")
            s8.add(card.art8 & "  ")
            s9.add(card.art9 & "  ")
    for s in [s0,s1,s2,s3,s4,s5,s6,s7,s8,s9]:
        echo s

proc print_all_suits() =
    for i in 1 .. 12:
        print_suit(i)
        echo ""

proc print_at_pos*(card:Card, xpos=1, ypos=1) =
    let arts = [ card.art0, card.art1, card.art2, card.art3, card.art4,
                 card.art5, card.art6, card.art7, card.art8, card.art9 ]
    var cur_x = xpos
    var cur_y = ypos

    for line in arts:
        if cur_y > max_screen_height: break
        move_cursor_to_pos(cur_x,cur_y)
        stdout.write(line)
        cur_y.inc(1)
