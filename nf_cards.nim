            s2.add(card.art2 & "  ")
            s3.add(card.art3 & "  ")
            s4.add(card.art4 & "  ")
            s5.add(card.art5 & "  ")
            s6.add(card.art6 & "  ")
            s7.add(card.art7 & "  ")
            s8.add(card.art8 & "  ")
            s9.add(card.art9 & "  ")
    for s in [s0,s1,s2,s3,s4,s5,s6,s7,s8,s9]:
        echo s

